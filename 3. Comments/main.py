#This is single-line comment in Python.
abc = 100 # This is single-line comment too.

"""
This is multi-line comment in Python.
It is used to explainn the part of the code.
"""

while True:
    if abc != 0: # this says if abc is not equal to 0.
        print(abc) #It will print abc
        abc = abc - 1  #And after it will be equal to abc-1
        """
        This code prints from 100 to 0.
        And this is just the explanation
        of the code and example of multi-line
        comments in Python.
        """
    elif abc == 0: #This says that if abc is equal to 0.
        break  #Break which is Python built-in function.
